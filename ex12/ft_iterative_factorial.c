int		ft_iterative_factorial(int nb)
{
	int c;
	int r;

	c = 1;
	r = 1;
	if (nb < 0 || nb > 16)
		return (0);
	while (c <= nb)
	{
		r = r * c;
		c++;
	}
	return (r);
}
